    private void populateProfile() {
        UserAccount User = system.getLoggedInUser();
        txtTitle.setText(User.getUsername());
        txtEmail.setText(User.getPerson().getEmail());
        txtPass.setText(User.getPassword());
        txtAddress.setText(User.getPerson().getAddress());
        txtNeighbourhood.setText(User.getPerson().getNeighbourhood().getName());
        txtCounty.setText(User.getPerson().getCounty().getName());     
        txtCity.setText(User.getPerson().getCity().getName());
        
    }

