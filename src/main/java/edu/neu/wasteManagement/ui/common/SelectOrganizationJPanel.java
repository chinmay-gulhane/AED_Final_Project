        private void loginLogoutChange(){
      for(Component comp: system.getHeader().getComponents()){
         JLabel jComp = (JLabel)comp;
         if(jComp.getText().equals("Sign Up"))
             jComp.setText("");
         if(jComp.getText().equals("Log In"))
             jComp.setText("Log Out");
        }
    }

