    private void initSetup() {        
          populateTable();
    }
      
    private void populateTable() {
    
       DefaultTableModel model = (DefaultTableModel) tblProducts.getModel();
       model.setRowCount(0);
    
        for (Product p : system.getProductsToSell(system.getLoggedInUser())) {
         Object[]row = new Object[6];
            row[0]= p;
            row[1]=p.getQuantity();
            row[2]=p.getPrice();
           
            //model will add row
            model.addRow(row);
          }
    }
  



      public void clearFields(){
        txtName.setText("");
        txtQuantity.setText("");
        txtPrice.setText("");   

    }

    private void resetPage() {
        populateTable();
        clearFields();
        initialButtonsState();
    }

    private void initialButtonsState() {
         btnPublish.setEnabled(false);
         btnUpdate.setEnabled(false);
    }


   public boolean checkValidate(){
        return Validations.isStringValid(txtName.getText()) &&
        Validations.isNumeric(txtQuantity.getText()) && 
        Validations.isNumeric(txtPrice.getText());
    }


