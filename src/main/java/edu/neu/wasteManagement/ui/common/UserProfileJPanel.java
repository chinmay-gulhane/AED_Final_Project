    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        String title = txtTitle.getText();
        String email = txtEmail.getText();
        String pass = txtPass.getText();
        
        String address = txtAddress.getText();
        String neighbourhood = txtNeighbourhood.getText();
        String county = txtCounty.getText();
        String city = txtCity.getText();                
        
        if(!checkValidate()){
            JOptionPane.showMessageDialog(this, "Please enter all the details!");
            return;
        }
        UserAccount User = system.getLoggedInUser();
        User.setUsername(title);
        
        if(!pass.equals(User.getPassword())){
        } else {
            try {
                User.setPassword(pass);
            } catch (Exception ex) {
                Logger.getLogger(UserProfileJPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        User.getPerson().setEmail(email);
        User.getPerson().setAddress(address);
        User.getPerson().getNeighbourhood().setName(neighbourhood);
        User.getPerson().getCounty().setName(county);
        User.getPerson().getCity().setName(city);
        User.getPerson().setName(title);
                        
        
//        this.system.addCourse(title, description, Integer.parseInt(credit), Double.parseDouble(price));
        JOptionPane.showMessageDialog(this, "Profile added!");

    }//GEN-LAST:event_btnSaveActionPerformed


