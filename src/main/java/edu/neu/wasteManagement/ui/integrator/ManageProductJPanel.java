    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:
        int selectedRowIndex = tblProducts.getSelectedRow();
        
        if(selectedRowIndex < 0){
            
        JOptionPane.showMessageDialog(this, "Please select a row to update");
            
        } else{
            
        DefaultTableModel model = (DefaultTableModel) tblProducts.getModel();
        selectedProduct = (Product)model.getValueAt(selectedRowIndex, 0);

       
        String name = txtName.getText();
        String quantity = txtQuantity.getText();
        String price = txtPrice.getText();
        
        if(!checkValidate()){
         JOptionPane.showMessageDialog(this, "Please enter valid details");
    }
        else{
        selectedProduct.setName(name);
        selectedProduct.setQuantity(Integer.parseInt(quantity));
        selectedProduct.setPrice(Integer.parseInt(price));
 
        JOptionPane.showMessageDialog(this, "Row updated successfully");
         resetPage();
            
        }
       
        }  
    }//GEN-LAST:event_btnUpdateMouseClicked

