    private void btnPublishActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPublishActionPerformed
        // TODO add your handling code here:
        int choice = JOptionPane.showConfirmDialog(this, "You are about to publish all product on Marketplace?");
        if( choice == 0){
            // Get Marketplace
            MarketplaceOrg market = (MarketplaceOrg) system.getOrganizationByType(Type.MARKETPLACE_ORG);
            for(Product prod: system.getProductsToSell(system.getLoggedInUser())){
                prod.setSeller(system.getLoggedInUser().getUsername());
                market.getCatalog().addProduct(prod);
            }
            
            JOptionPane.showMessageDialog(this, "Products published on Marketplace!");
            
            // Empty product catalog of loggedInUser's organization
            system.getOrganizationByUserAccount(system.getLoggedInUser()).getProductCatalog().setProducts(new ArrayList<Product>());
        
            initSetup();
        }
 
    }//GEN-LAST:event_btnPublishActionPerformed

